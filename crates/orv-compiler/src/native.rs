fn push_native_captured_json_value(
    source: &mut String,
    lookup_expr: &str,
    value_kind: &str,
    operation: NativeCapturedJsonOperation<'_>,
    kinds: &NativeCapturedJsonKinds<'_>,
    response_origin_id: &str,
) -> bool {
    match value_kind {
        kind if kind == kinds.string_kind => {
            if operation.op.is_some() {
                return push_native_string_operation_json_value(source, lookup_expr, operation);
            }
            let _ = writeln!(
                source,
                "        orv_native_push_json_string({lookup_expr}.unwrap_or(\"\"), &mut body);"
            );
            true
        }
        kind if kind == kinds.int_kind => {
            let _ = writeln!(
                source,
                "        match {lookup_expr}.unwrap_or(\"\").trim().parse::<i64>() {{"
            );
            if !push_native_int_success_arm(
                source,
                operation,
                kinds.error_prefix,
                response_origin_id,
            ) {
                return false;
            }
            source.push_str("            Err(_) => {\n");
            let error_body = format!(r#"{{"error":"{} int cast failed"}}"#, kinds.error_prefix);
            let body_expr = format!("{}.to_string()", rust_string_literal(&error_body));
            push_native_handler_response_return(source, 500, &body_expr, response_origin_id);
            source.push_str("            },\n        }\n");
            true
        }
        kind if kind == kinds.float_kind => {
            let _ = writeln!(
                source,
                "        match {lookup_expr}.unwrap_or(\"\").trim().parse::<f64>() {{"
            );
            if !push_native_float_success_arm(
                source,
                operation,
                kinds.error_prefix,
                response_origin_id,
            ) {
                return false;
            }
            source.push_str("            _ => {\n");
            let error_body = format!(r#"{{"error":"{} float cast failed"}}"#, kinds.error_prefix);
            let body_expr = format!("{}.to_string()", rust_string_literal(&error_body));
            push_native_handler_response_return(source, 500, &body_expr, response_origin_id);
            source.push_str("            },\n        }\n");
            true
        }
        kind if kind == kinds.bool_kind => {
            if let Some((true_json, false_json)) = native_bool_json_outputs(operation) {
                let _ = writeln!(
                    source,
                    "        match {lookup_expr}.unwrap_or(\"\").trim() {{"
                );
                let _ = writeln!(
                    source,
                    "            \"true\" => body.push_str(\"{true_json}\"),\n            \"false\" => body.push_str(\"{false_json}\"),\n            _ => {{"
                );
                let error_body =
                    format!(r#"{{"error":"{} bool cast failed"}}"#, kinds.error_prefix);
                let body_expr = format!("{}.to_string()", rust_string_literal(&error_body));
                push_native_handler_response_return(source, 500, &body_expr, response_origin_id);
                source.push_str("            },\n        }\n");
                return true;
            }
            push_native_bool_captured_operand_json_value(
                source,
                lookup_expr,
                operation,
                kinds.error_prefix,
                response_origin_id,
            )
        }
        _ => false,
    }
}

fn native_bool_json_outputs(
    operation: NativeCapturedJsonOperation<'_>,
) -> Option<(&'static str, &'static str)> {
    let NativeCapturedJsonOperation {
        op,
        operand_json,
        operand_kind,
        operand_name,
        ..
    } = operation;
    let invert = match (op, operand_json, operand_kind, operand_name) {
        (None, None, None, None) => false,
        (Some("not"), None, None, None) => true,
        (
            Some(op @ ("eq" | "ne" | "and" | "or" | "not_and" | "not_or" | "and_not" | "or_not")),
            Some(operand @ ("true" | "false")),
            None,
            None,
        ) => {
            return Some(native_bool_json_pair(
                native_bool_binary_result(op, true, operand == "true")?,
                native_bool_binary_result(op, false, operand == "true")?,
            ));
        }
        _ => return None,
    };
    Some(native_bool_json_pair(!invert, invert))
}

const fn native_bool_json_pair(
    result_when_true: bool,
    result_when_false: bool,
) -> (&'static str, &'static str) {
    (
        if result_when_true { "true" } else { "false" },
        if result_when_false { "true" } else { "false" },
    )
}

fn native_bool_binary_result(op: &str, left: bool, right: bool) -> Option<bool> {
    match op {
        "eq" => Some(left == right),
        "ne" => Some(left != right),
        "and" => Some(left && right),
        "or" => Some(left || right),
        "not_and" => Some(!left && right),
        "not_or" => Some(!left || right),
        "and_not" => Some(left && !right),
        "or_not" => Some(left || !right),
        _ => None,
    }
}

fn push_native_bool_captured_operand_json_value(
    source: &mut String,
    lookup_expr: &str,
    operation: NativeCapturedJsonOperation<'_>,
    error_prefix: &str,
    response_origin_id: &str,
) -> bool {
    let NativeCapturedJsonOperation {
        op,
        operand_json,
        operand_kind,
        operand_name,
        ..
    } = operation;
    let (
        Some(op @ ("eq" | "ne" | "and" | "or" | "not_and" | "not_or" | "and_not" | "or_not")),
        None,
        Some(operand_kind),
        Some(operand_name),
    ) = (op, operand_json, operand_kind, operand_name)
    else {
        return false;
    };
    let Some(operand_lookup) = native_bool_operand_lookup_expr(operand_kind, operand_name) else {
        return false;
    };
    let _ = writeln!(
        source,
        "        match ({lookup_expr}.unwrap_or(\"\").trim(), {operand_lookup}.unwrap_or(\"\").trim()) {{"
    );
    for (left, right) in [
        ("true", "true"),
        ("true", "false"),
        ("false", "true"),
        ("false", "false"),
    ] {
        let Some(result) = native_bool_binary_result(op, left == "true", right == "true") else {
            return false;
        };
        let result_json = if result { "true" } else { "false" };
        let _ = writeln!(
            source,
            "            ({}, {}) => body.push_str({}),",
            rust_string_literal(left),
            rust_string_literal(right),
            rust_string_literal(result_json)
        );
    }
    source.push_str("            _ => {\n");
    let error_body = format!(r#"{{"error":"{error_prefix} bool cast failed"}}"#);
    let body_expr = format!("{}.to_string()", rust_string_literal(&error_body));
    push_native_handler_response_return(source, 500, &body_expr, response_origin_id);
    source.push_str("            },\n        }\n");
    true
}

fn push_native_string_operation_json_value(
    source: &mut String,
    lookup_expr: &str,
    operation: NativeCapturedJsonOperation<'_>,
) -> bool {
    match operation.op {
        Some("eq" | "ne") => {
            push_native_string_comparison_json_value(source, lookup_expr, operation)
        }
        Some("concat") => push_native_string_concat_json_value(source, lookup_expr, operation),
        Some("concat_prefix") => {
            push_native_string_prefix_concat_json_value(source, lookup_expr, operation)
        }
        Some("concat_affix") => {
            push_native_string_affix_concat_json_value(source, lookup_expr, operation)
        }
        Some("concat_join") => {
            push_native_string_join_concat_json_value(source, lookup_expr, operation)
        }
        _ => false,
    }
}

fn push_native_string_comparison_json_value(
    source: &mut String,
    lookup_expr: &str,
    operation: NativeCapturedJsonOperation<'_>,
) -> bool {
    let NativeCapturedJsonOperation {
        op,
        operand_json,
        operand_kind,
        operand_name,
        ..
    } = operation;
    let Some(operator) = native_comparison_operator(op) else {
        return false;
    };
    source.push_str("        {\n");
    let _ = writeln!(
        source,
        "            let value = {lookup_expr}.unwrap_or(\"\");"
    );
    match (operand_json, operand_kind, operand_name) {
        (Some(operand), None, None) => {
            let _ = writeln!(
                source,
                "            let operand = {};",
                rust_string_literal(operand)
            );
        }
        (None, Some(kind), Some(name)) => {
            let Some(operand_lookup) = native_string_operand_lookup_expr(kind, name) else {
                return false;
            };
            let _ = writeln!(
                source,
                "            let operand = {operand_lookup}.unwrap_or(\"\");"
            );
        }
        _ => return false,
    }
    push_native_comparison_json_result(source, "value", operator, "operand", "            ");
    source.push_str("        }\n");
    true
}

fn push_native_string_concat_json_value(
    source: &mut String,
    lookup_expr: &str,
    operation: NativeCapturedJsonOperation<'_>,
) -> bool {
    let NativeCapturedJsonOperation {
        op,
        operand_json,
        operand_kind,
        operand_name,
        ..
    } = operation;
    if op != Some("concat") {
        return false;
    }
    source.push_str("        {\n");
    let _ = writeln!(
        source,
        "            let mut value = String::from({lookup_expr}.unwrap_or(\"\"));"
    );
    match (operand_json, operand_kind, operand_name) {
        (Some(operand), None, None) => {
            let _ = writeln!(
                source,
                "            value.push_str({});",
                rust_string_literal(operand)
            );
        }
        (None, Some(kind), Some(name)) => {
            let Some(operand_lookup) = native_string_operand_lookup_expr(kind, name) else {
                return false;
            };
            let _ = writeln!(
                source,
                "            let operand = {operand_lookup}.unwrap_or(\"\");"
            );
            source.push_str("            value.push_str(operand);\n");
        }
        _ => return false,
    }
    source.push_str("            orv_native_push_json_string(&value, &mut body);\n");
    source.push_str("        }\n");
    true
}

