fn orv_native_route_path_params(pattern: &'static str, path: &str) -> Option<Vec<OrvNativeParam>> {
    if pattern == "*" {
        return Some(Vec::new());
    }
    let pattern_segments: Vec<&str> = pattern.split('/').collect();
    let path_segments: Vec<&str> = path.split('/').collect();
    if let Some(rest_segment) = pattern_segments.last() {
        if rest_segment
            .strip_prefix(':')
            .and_then(|segment| segment.strip_suffix('*'))
            .is_some()
        {
            let prefix_len = pattern_segments.len() - 1;
            if path_segments.len() <= prefix_len {
                return None;
            }
            let mut params = Vec::new();
            for (pattern_segment, path_segment) in pattern_segments
                .iter()
                .take(prefix_len)
                .zip(path_segments.iter())
            {
                if !orv_native_match_route_segment(pattern_segment, path_segment, &mut params) {
                    return None;
                }
            }
            let name = rest_segment
                .strip_prefix(':')
                .and_then(|segment| segment.strip_suffix('*'))
                .unwrap_or("")
                .to_string();
            params.push(OrvNativeParam {
                name,
                value: path_segments[prefix_len..].join("/"),
            });
            return Some(params);
        }
    }
    if pattern_segments.len() != path_segments.len() {
        return None;
    }
    let mut params = Vec::new();
    for (pattern_segment, path_segment) in pattern_segments.iter().zip(path_segments.iter()) {
        if !orv_native_match_route_segment(pattern_segment, path_segment, &mut params) {
            return None;
        }
    }
    Some(params)
}

fn orv_native_match_route_segment(
    pattern_segment: &str,
    path_segment: &str,
    params: &mut Vec<OrvNativeParam>,
) -> bool {
    let Some((name, suffix)) = orv_native_route_param_segment(pattern_segment) else {
        return pattern_segment == path_segment;
    };
    let Some(value) = path_segment.strip_suffix(suffix) else {
        return false;
    };
    params.push(OrvNativeParam {
        name: name.to_string(),
        value: value.to_string(),
    });
    true
}

fn orv_native_route_param_segment(segment: &str) -> Option<(&str, &str)> {
    let body = segment.strip_prefix(':')?;
    let end = body
        .char_indices()
        .find_map(|(index, ch)| (!orv_native_route_param_name_char(ch)).then_some(index))
        .unwrap_or(body.len());
    (end > 0).then_some((&body[..end], &body[end..]))
}

