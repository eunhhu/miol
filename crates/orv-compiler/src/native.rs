fn push_native_response_condition(
    source: &mut String,
    condition: &ServerResponseConditionArtifact,
) -> bool {
    if push_native_float_response_condition(source, condition) {
        return true;
    }
    if push_native_int_response_condition(source, condition) {
        return true;
    }
    if push_native_bool_response_condition(source, condition) {
        return true;
    }
    let Some((lookup, operator)) = native_response_condition_lookup(condition.kind.as_str()) else {
        return false;
    };
    if let Some(operand_name) = condition.operand_name.as_deref() {
        let operand_lookup = condition
            .operand_kind
            .as_deref()
            .and_then(native_response_condition_operand_lookup)
            .unwrap_or(lookup);
        let _ = writeln!(
            source,
            "        if {lookup}(route_match, {}) {operator} {operand_lookup}(route_match, {}) {{",
            rust_string_literal(&condition.name),
            rust_string_literal(operand_name)
        );
    } else {
        let _ = writeln!(
            source,
            "        if {lookup}(route_match, {}) {operator} Some({}) {{",
            rust_string_literal(&condition.name),
            rust_string_literal(&condition.value)
        );
    }
    true
}

fn push_native_bool_response_condition(
    source: &mut String,
    condition: &ServerResponseConditionArtifact,
) -> bool {
    let Some((lookup, operator)) = native_response_condition_bool_lookup(condition.kind.as_str())
    else {
        return false;
    };
    if condition.operand_name.is_some() {
        let Some(operand_lookup) = condition
            .operand_kind
            .as_deref()
            .and_then(native_response_condition_bool_operand_lookup)
        else {
            return false;
        };
        let operand_name = condition.operand_name.as_deref().unwrap_or_default();
        let left_prefix = native_response_condition_bool_left_prefix(condition.kind.as_str());
        let right_prefix = native_response_condition_bool_right_prefix(condition.kind.as_str());
        let _ = writeln!(
            source,
            "        if match ({lookup}(route_match, {}).unwrap_or(\"\").trim(), {operand_lookup}(route_match, {}).unwrap_or(\"\").trim()) {{",
            rust_string_literal(&condition.name),
            rust_string_literal(operand_name)
        );
        let _ = writeln!(
            source,
            "            (\"true\", \"true\") => {left_prefix}true {operator} {right_prefix}true,"
        );
        let _ = writeln!(
            source,
            "            (\"true\", \"false\") => {left_prefix}true {operator} {right_prefix}false,"
        );
        let _ = writeln!(
            source,
            "            (\"false\", \"true\") => {left_prefix}false {operator} {right_prefix}true,"
        );
        let _ = writeln!(
            source,
            "            (\"false\", \"false\") => {left_prefix}false {operator} {right_prefix}false,"
        );
        source.push_str("            _ => false,\n        } {\n");
        return true;
    }
    let value = match condition.value.as_str() {
        "true" | "false" => condition.value.as_str(),
        _ => return false,
    };
    let _ = writeln!(
        source,
        "        if match {lookup}(route_match, {}).unwrap_or(\"\").trim() {{",
        rust_string_literal(&condition.name)
    );
    let left_prefix = native_response_condition_bool_left_prefix(condition.kind.as_str());
    let _ = writeln!(
        source,
        "            \"true\" => {left_prefix}true {operator} {value},"
    );
    let _ = writeln!(
        source,
        "            \"false\" => {left_prefix}false {operator} {value},"
    );
    source.push_str("            _ => false,\n        } {\n");
    true
}

